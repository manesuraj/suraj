package Abstraction;

public class Simple {
	
	private long id;
	private String name;
	private int age;
	
	public void setId(long id) {
		this.id=id;
	}
	
	public void setName(String name) {
		this.name=name;
	}
	
	public void setAge(int age) {
		this.age=age;
	}
	
	public long getId() {
		return this.id;
	}
	
	public String getName() {
		return this.name;
	}
	
	public int getAge() {
		return this.age=age;
	}

}
